package com.real_estate.agent.model;

public class Customer {

	
	private Integer C_Num;
	private Integer C_Age;
	private String C_Name;
	private String C_Gender;
	private String C_Tel;
	private String C_Content;
	private String C_Form;
	
	
	public String getC_Form() {
		return C_Form;
	}
	public void setC_Form(String c_Form) {
		C_Form = c_Form;
	}
	public int getC_Num() {
		return C_Num;
	}
	public void setC_Num(Integer c_Num) {
		C_Num = c_Num;
	}
	public Integer getC_Age() {
		return C_Age;
	}
	public void setC_Age(Integer c_Age) {
		C_Age = c_Age;
	}
	public String getC_Name() {
		return C_Name;
	}
	public void setC_Name(String c_Name) {
		C_Name = c_Name;
	}
	public String getC_Gender() {
		return C_Gender;
	}
	public void setC_Gender(String c_Gender) {
		C_Gender = c_Gender;
	}
	public String getC_Tel() {
		return C_Tel;
	}
	public void setC_Tel(String c_Tel) {
		C_Tel = c_Tel;
	}
	public String getC_Content() {
		return C_Content;
	}
	public void setC_Content(String c_Content) {
		C_Content = c_Content;
	}
	
	
}
